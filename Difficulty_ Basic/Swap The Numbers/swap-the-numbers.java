import java.io.*;
import java.util.*;

class GFG {
    public static void main(String args[]) {
        Scanner sc = new Scanner(System.in);
        int a = sc.nextInt();
        int b = sc.nextInt();

        // Swap the values
        int temp = a;
        a = b;
        b = temp;

        System.out.println(a + " " + b);
    }
}



